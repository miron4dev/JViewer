CREATE TABLE IF NOT EXISTS news (
   id INTEGER PRIMARY KEY AUTOINCREMENT UNIQUE NOT NULL,
   topic TEXT NOT NULL UNIQUE,
   text TEXT NOT NULL,
   date DATETIME NOT NULL,
   author TEXT NOT NULL
)